// 🧠 Apply translations based on selected language
function applyTranslations(lang) {
  const data = translations[lang];
  if (!data) return;

  for (const key in data) {
    const element = document.getElementById(key);
    if (element) {
      if (element.hasAttribute("placeholder")) {
        element.placeholder = data[key];
      } else if (iconElements[key]) {
        element.innerHTML = iconElements[key] + data[key];
      } else {
        element.textContent = data[key];
      }
    }
  }
}

// 🌐 On page load, apply saved or default language
window.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("language") || "en";
  const langSelector = document.getElementById("languageSelector");
  if (langSelector) langSelector.value = savedLang;
  applyTranslations(savedLang);
});

// 🌐 When language changes
const langSelector = document.getElementById("languageSelector");
if (langSelector) {
  langSelector.addEventListener("change", function () {
    const lang = this.value;
    localStorage.setItem("language", lang);
    applyTranslations(lang);
  });
}

// Option 1: Rename to applyTranslations (to match HTML)
function applyTranslations(lang) {
  document.querySelectorAll("[data-translate]").forEach(element => {
    const key = element.getAttribute("data-translate");
    if (translations[lang] && translations[lang][key]) {
      // Handle different element types
      if (element.hasAttribute("placeholder")) {
        // For input/textarea placeholders
        element.setAttribute("placeholder", translations[lang][key]);
      } else if (element.hasAttribute("data-placeholder")) {
        // For elements with data-placeholder (like your input fields)
        const placeholderKey = element.getAttribute("data-placeholder");
        if (translations[lang][placeholderKey]) {
          element.setAttribute("placeholder", translations[lang][placeholderKey]);
        }
      } else {
        // For regular elements with text content
        element.textContent = translations[lang][key];
      }
    }
  });
  document.querySelectorAll("[data-translate]").forEach(el => {
    const key = el.getAttribute("data-translate");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // 2. Translate input placeholders
  document.querySelectorAll("[data-placeholder]").forEach(input => {
    const key = input.getAttribute("data-placeholder");
    if (translations[lang] && translations[lang][key]) {
      input.placeholder = translations[lang][key];
    }
  });
}








